    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        if (user.getText().equals("admin") && pass.getText().equals("admin")) {
            dispose();
            JOptionPane.showMessageDialog(null, "Sesion Iniciada");
            new ModuloAdmin().setVisible(true);
        } else if (user.getText().isEmpty() && pass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Usuario y contrase√±a incorrectos");
        } else {
            for (Vendedor vn : Vendedor.cuentasVendedores) {
                if (user.getText().equals(vn.getNombre()) && pass.getText().equals(vn.getPass())) {
                    dispose();
                    JOptionPane.showMessageDialog(null, "Sesion Iniciada");
                    new ModuloVendedor().setVisible(true);
                }
            }

        }
    }//GEN-LAST:event_loginActionPerformed

