    public int TraerPosicion(int id) {
        posicionTabla = id;

        return posicionTabla;
    }

    public void ActualizarDatos() {
        Cliente.cuentasClientes.get(posicionTabla).setNombre(nombre.getText());
        Cliente.cuentasClientes.get(posicionTabla).setNit(Integer.parseInt(nit.getText()));
        Cliente.cuentasClientes.get(posicionTabla).setCorreo(correo.getText());
        Cliente.cuentasClientes.get(posicionTabla).setGenero(genero.getText());
    }

    public void ImportarDatos() {

        codigo.setText(Cliente.cuentasClientes.get(posicionTabla).getCodigo() + "");
        nombre.setText(Cliente.cuentasClientes.get(posicionTabla).getNombre() + "");
        nit.setText(Cliente.cuentasClientes.get(posicionTabla).getNit() + "");
        genero.setText(Cliente.cuentasClientes.get(posicionTabla).getGenero() + "");
        correo.setText(Cliente.cuentasClientes.get(posicionTabla).getCorreo() + "");
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (correo.getText().equals("") || nombre.getText().equals("") || nit.getText().equals("") || genero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
        } else {
            int op = JOptionPane.showOptionDialog(null, "Â¿Estas seguro de actualizar los datos?", "title", JOptionPane.YES_OPTION, JOptionPane.CANCEL_OPTION, null, null, EXIT_ON_CLOSE);
            if (op == 0) {
                ActualizarDatos();
                dispose();
                JOptionPane.showMessageDialog(null, "Datos actualizados exitosamente.");
            } else {
                dispose();
                JOptionPane.showMessageDialog(null, "No se ha realizado ningun cambio en el cliente.");
            }
            new ModuloAdmin().setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

