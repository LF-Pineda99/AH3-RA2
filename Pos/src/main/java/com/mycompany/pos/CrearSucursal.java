    public void GuardarSucursal() {
        if (codigo.getText().equals("") || nombre.getText().equals("") || direccion.getText().equals("") || correo.getText().equals("") || telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos");
        } else {
            int op = JOptionPane.showOptionDialog(null, "Â¿Estas seguro de crear esta sucursal?", "title", JOptionPane.YES_OPTION, JOptionPane.CANCEL_OPTION, null, null, EXIT_ON_CLOSE);
            if (op == 0) {
                Sucursal.cuentasSucursales.add(new Sucursal(Integer.parseInt(codigo.getText()), nombre.getText(), direccion.getText(), correo.getText(), Integer.parseInt(telefono.getText())));
                JOptionPane.showMessageDialog(null, "Sucursal creada exitosamente.");
            } else {
                JOptionPane.showMessageDialog(null, "No se ha creado ninguna sucursal.");
            }
            dispose();
            new ModuloAdmin().setVisible(true);
        }
    }

